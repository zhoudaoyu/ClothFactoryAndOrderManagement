#!/usr/bin/env python

__metaclass__ = type

class Cloth:
	def __init__(self, style, size, color):
		self.style = style
		self.size = size
		self.color = color
		
	def setSize(self, size):
		self.size = size
		
	def getSize(self):
		return self.size

	def setColor(self, size):
		self.color = color
		
	def getColor(self):
		return self.color
			
	def setStyle(self, size):
		self.color = color
		
	def getStyle(self):
		return self.color
						